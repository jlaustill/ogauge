/* Helper: create image widget with needle source, pivot, and centered alignment.
 * The image must be aligned so the pivot lands at the scale center.
 * Offset from center = (img_w/2 - pivot_x, img_h/2 - pivot_y). */
static inline lv_obj_t * lvgl_helper_create_needle_img(lv_obj_t * parent)
{
    lv_obj_t * img = lv_image_create(parent);
    lv_image_set_src(img, &needle_img_dsc);
    lv_image_set_pivot(img, NEEDLE_IMG_PIVOT_X, NEEDLE_IMG_PIVOT_Y);
    lv_obj_align(img, LV_ALIGN_CENTER,
                 NEEDLE_IMG_W / 2 - NEEDLE_IMG_PIVOT_X,
                 NEEDLE_IMG_H / 2 - NEEDLE_IMG_PIVOT_Y);
    return img;
}

